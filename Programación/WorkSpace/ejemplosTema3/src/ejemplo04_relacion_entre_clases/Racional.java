package ejemplo04_relacion_entre_clases;

public class Racional {

	private int num;
	private int den;
	
	
	
	public Racional(int num, int den) {
		super();
		this.num = num;
		this.den = den;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getDen() {
		return den;
	}
	public void setDen(int den) {
		this.den = den;
	}
	
	
	
	
	
}
